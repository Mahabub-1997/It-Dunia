            <h1>Cybersecurity and Your New Remote Workforce</h1>
            <p>Ed Moyle</p>
            <section class="block">
                <p>For most organizations, COVID-19 has been a literal transformative agent. Our organizations have almost overnight gone from environments where teleworking was an exception, to where it's the norm.

We've gone from selective, partial externalization of key services, to near-total externalization. We've shifted from BYOD being accepted -- though perhaps grudgingly -- to where it's a key part of keeping the business operational. In short, remote work is the new normal.

As any student of human nature will tell you, people tend to view "the new" with reservation. There's a temptation when things are new to assume the worst about them. For security pros, this means we often view new things as riskier than things we're familiar with.

For those who have been in the industry for a few years, recall the concerns many security pros had when cloud technologies were first gaining traction. Or, before that, when OS virtualization was first making inroads into datacenters. </p>
                <p> This is not to say that there aren't risks -- just that when something is new, we tend to expand the risks in our minds. Over time, while recognizing new risks, we realize that changes can lead to improvements.

For example, in the early days of virtualization, risk areas seemed quite pronounced: we worried about virtual machine escape, segmentation attacks, mobility of workloads, etc. Over time, we learned that while these risks are real, so too are opportunities for beneficial impacts. For instance, using snapshots to aid patching efforts, using orchestration to help automate policy enforcement, and leveraging clones for security testing, i.e. letting us test more thoroughly without disrupting production.

The point is, it's the rare event or technology that is entirely downside or upside from a security point of view. All of this may seem obvious, but I'm bringing it up because there has been significant attention paid in the trade press about ways remote access negatively impacts security. This view is not necessarily false. There absolutely can be ways an externalized workforce can exacerbate unwanted security outcomes. That said, there are potential upsides too -- ways to harness the remote work to, over the long term, improve security posture. </p>

