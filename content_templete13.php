            <h1>Qubits Hold Quantum Information</h1>
            <p>Tohoku University</p>
            <section class="block">
                <p>Quantum bits, or qubits, can hold quantum information much longer now thanks to efforts by an international research team. The researchers have increased the retention time, or coherence time, to 10 milliseconds — 10,000 times longer than the previous record — by combining the orbital motion and spinning inside an atom. Such a boost in information retention has major implications for information technology developments since the longer coherence time makes spin-orbit qubits the ideal candidate for building large quantum computers.

They published their results on July 20, 2020, in Nature Materials.

“We defined a spin-orbit qubit using a charged particle, which appears as a hole, trapped by an impurity atom in silicon crystal,” said lead author Dr. Takashi Kobayashi, research scientist at the University of New South Wales Sydney and assistant professor at Tohoku University. “Orbital motion and spinning of the hole are strongly coupled and locked together. This is reminiscent of a pair of meshing gears where circular motion and spinning are locked together.”

Qubits have been encoded with spin or orbital motion of a charged particle, producing different advantages that are highly demanded for building quantum computers. To utilize the advantages of qubits, Kobayashi and the team specifically used an exotic charged particle “hole” in silicon to define a qubit, since orbital motion and spin of holes in silicon are coupled together.</p>
                <p>Spin-orbit qubits encoded by holes are particularly sensitive to electric fields, according to Kobayashi, which allows for more rapid control and benefits scaling up quantum computers. However, the qubits are affected by electrical noise, limiting their coherence time.

“In this work, we have engineered sensitivity to the electric field of our spin-orbit qubit by stretching the silicon crystal like a rubber band,” Kobayashi said. “This mechanical engineering of the spin-orbit qubit enables us to remarkably extend its coherence time, while still retaining moderate electrical sensitivity to control the spin-orbit qubit.”

Think of gears in a watch. Their individual spinning propels the entire mechanism to keep time. It is neither the spin nor orbital motion, but a combination of them that takes the information forward.

“These results open a pathway to develop new artificial quantum systems and to improve the functionality and scalability of spin-based quantum technologies,” Kobayashi said.</p>

