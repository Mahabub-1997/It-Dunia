            <h1>Apple's AR Glasses Be Ready </h1>
            <p> John P. Mello Jr</p>
            <section class="block">
                <p>Apple may launch an augmented reality line of smart glasses in the spring of 2021, according to Jon Prosser, host of the video blog Front Page Tech.

The new peepers will be called "Apple Glass" and sell for US$499, with prescription lenses costing more, Prosser claimed.

Both lenses are displays that support gesture interaction.

The glasses will work in conjunction with an iPhone.

Early prototypes supported the LiDAR sensor for 3D scanning and wireless charging, said Prosser.

Apple originally planned to unveil the specs at its fall event, but it may postpone the announcement until March 2021, with release planned for late 2021 or early 2022, he added.

"These rumors have been building up for quite some time, but this is the most cohesive information we've had on this so far," said George Jijiashvili, senior analyst at Omdia, a research and consulting firm in London.

"I think Apple has been working on AR glasses behind closed doors, and they will release them because they have all the right pieces to make it work," he told TechNewsWorld. </p>
                <p> Incorporating gesture control into the glasses is a good move, observed San Jose, California-based Kevin Krewell, principal analyst at Tirias Research, a high-tech research and advisory firm.

"Gesture control allows the Apple Glass to be controlled without resorting to using a controller that is easily lost," he told TechNewsWorld.

"Gesture control is a key control method in Microsoft's HoloLens today," Krewell added.

At this stage of smart glasses development, pairing them with a smartphone is an important design choice, noted David MacQueen, executive director for the global wireless practice at Strategy Analytics, a research, advisory and analytics firm based in Newton, Massachusetts.

"The only way to get the price point down to a consumer-friendly level is to offload as much of the processing power, sensors and such to a paired phone," he told TechNewsWorld.

"Nreal and Samsung teased similar 'smartphone paired' AR glasses at CE earlier this year," MacQueen added.

Most smart devices depend on a smartphone, at least for initial setup, said Julie Ask, principal analyst at Forrester Research, a market research company headquartered in Cambridge, Massachusetts.

"A smartphone dependency will permit the glasses to be wireless like AirPods or similar devices," she told TechNewsWorld</p>

