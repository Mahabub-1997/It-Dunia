            <h1>World’s Largest Digital Camera Snaps</h1>
            <p>C. Rubin </p>
            <section class="block">
                <p>he camera will explore cosmic mysteries as part of the Rubin Observatory’s Legacy Survey of Space and Time.

Crews at the Department of Energy’s SLAC National Accelerator Laboratory have taken the first 3,200-megapixel digital photos – the largest ever taken in a single shot – with an extraordinary array of imaging sensors that will become the heart and soul of the future camera of Vera C. Rubin Observatory.

The images are so large that it would take 378 4K ultra-high-definition TV screens to display one of them in full size, and their resolution is so high that you could see a golf ball from about 15 miles away. These and other properties will soon drive unprecedented astrophysical research.</p>
                <p>Next, the sensor array will be integrated into the world’s largest digital camera, currently under construction at SLAC. Once installed at Rubin Observatory in Chile, the camera will produce panoramic images of the complete Southern sky – one panorama every few nights for 10 years.

Its data will feed into the Rubin Observatory Legacy Survey of Space and Time (LSST) – a catalog of more galaxies than there are living people on Earth and of the motions of countless astrophysical objects. Using the LSST Camera, the observatory will create the largest astronomical movie of all time and shed light on some of the biggest mysteries of the universe, including dark matter and dark energy.

The first images taken with the sensors were a test for the camera’s focal plane, whose assembly was completed at SLAC in January.

“This is a huge milestone for us,” said Vincent Riot, LSST Camera project manager from DOE’s Lawrence Livermore National Laboratory. ”The focal plane will produce the images for the LSST, so it’s the capable and sensitive eye of the Rubin Observatory.”

SLAC’s Steven Kahn, director of the observatory, said, “This achievement is among the most significant of the entire Rubin Observatory Project. The completion of the LSST Camera focal plane and its successful tests is a huge victory by the camera team that will enable Rubin Observatory to deliver next-generation astronomical science.”</p>

