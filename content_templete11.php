            <h1>Battery-Free Game Boy Runs Forever</h1>
            <p>Marcel Krijger</p>
            <section class="block">
                <p>A hand-held video game console allowing indefinite gameplay might be a parent’s worst nightmare.

But this Game Boy is not just a toy. It’s a powerful proof-of-concept, developed by researchers at Northwestern University and the Delft University of Technology (TU Delft) in the Netherlands, that pushes the boundaries of battery-free intermittent computing into the realm of fun and interaction. It is also another step toward sustainable technology.

“It’s the first battery-free interactive device that harvests energy from user actions,” said Northwestern’s Josiah Hester, who co-led the research. “When you press a button, the device converts that energy into something that powers your gaming.”

“Sustainable gaming will become a reality, and we made a major step in that direction — by getting rid of the battery completely,” said TU Delft’s Przemyslaw Pawelczak, who co-led the research with Hester. “With our platform, we want to make a statement that it is possible to make a sustainable gaming system that brings fun and joy to the user.”

The teams will present the research virtually at UbiComp 2020, a major conference within the field of interactive systems, on September 15, 2020.

Hester is an assistant professor of electrical and computer engineering and computer science in Northwestern’s McCormick School of Engineering. Pawelczak is an assistant professor in the Embedded Software Lab at TU Delft. Their team includes Jasper de Winkel and Vito Kortbeek, both Ph.D. candidates at TU Delft.</p>
                <p>To ensure an acceptable duration of gameplay between power failures, the researchers had to design the system hardware and software from the ground up to be energy aware as well as very energy efficient. They also developed a new technique of storing the system state in non-volatile memory, minimizing overhead and allowing quick restoration when power returns. No more need for save games as seen in traditional platforms, as the player can now continue gameplay from the exact point of the device fully losing power – even if it is mid-jump in a platform game such as Super Mario Land.</p>

